function getDayOfWeekNumberJS()
{
    let currentDate = new Date();

    // sunday returns as 0
    let dayOfWeekNumber = currentDate.getDay();

    // adjust sunday to return as 1
    dayOfWeekNumberAdjusted = dayOfWeekNumber + 1;

    return dayOfWeekNumberAdjusted;
}

