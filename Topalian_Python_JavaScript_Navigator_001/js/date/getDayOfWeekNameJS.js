function getDayOfWeekNameJS()
{
    let currentDate = new Date();

    // 0-6
    let dayNumber = currentDate.getDay();

    let daysOfWeek = ["Sunday", "Monday", "Tuesday", "Wednesday", "Thursday", "Friday", "Saturday"];

    let dayName = daysOfWeek[dayNumber];

    return dayName;
}

